    # ── 산업 분류 ───────────────────────────────────────────────────

    def _classify_sector(self, title: str, firm_nm: str, stock_names: list) -> str:
        """키워드 사전 기반 산업 분류 (대형주 폴백 매핑 포함)"""
        # 1) 종목명 매핑을 통한 산업 분류 (대형주 폴백 우선 시도)
        for stock in stock_names:
            # STOCK_TO_SECTOR에 사전 선언된 대표주-산업 맵 참조
            if stock in STOCK_TO_SECTOR:
                return STOCK_TO_SECTOR[stock]
            
            # SECTOR_KEYWORDS 사전과 직접 일치하는지 시도
            for keyword, sector in SECTOR_KEYWORDS.items():
                if stock == keyword:
                    return sector

        # 2) 제목에서 직접 산업 키워드 매칭 (더 긴 키워드 우선)
        scored = []
        for keyword, sector in SECTOR_KEYWORDS.items():
            if keyword in title:
                # 더 긴 키워드에 더 높은 점수 부여
                scored.append((len(keyword), sector))

        if scored:
            scored.sort(reverse=True)
            return scored[0][1]

        return ""

    # ── 태그 추출 ───────────────────────────────────────────────────

    def _extract_tags(self, title: str) -> list:
        """패턴 매칭으로 태그 추출"""
        tags = []
        for pattern, tag in COMPILED_TAG_PATTERNS:
            if pattern.search(title):
                if tag not in tags:
                    tags.append(tag)
        return tags

    # ── 액션 타입 ───────────────────────────────────────────────────

    def _classify_action(self, title: str) -> Optional[str]:
        """리포트 유형 분류"""
        for pattern, action in COMPILED_ACTION_PATTERNS:
            if pattern.search(title):
                return action
        return None

